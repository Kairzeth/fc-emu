#[derive(Clone, Copy, Debug, PartialEq, Eq)]
pub struct Instruction {
    pub opcode: u8,
    pub mnemonic: &'static str,
    pub cycles: u8,
}

pub fn decode(opcode: u8) -> Option<Instruction> {
    let (mnemonic, cycles) = match opcode {
        0x00 => ("BRK", 7),
        0xea => ("NOP", 2),
        0x18 => ("CLC", 2),
        0x38 => ("SEC", 2),
        0x58 => ("CLI", 2),
        0x78 => ("SEI", 2),
        0xb8 => ("CLV", 2),
        0xd8 => ("CLD", 2),
        0xf8 => ("SED", 2),
        0xa9 | 0xa5 | 0xb5 | 0xad | 0xbd | 0xb9 | 0xa1 | 0xb1 => ("LDA", 2),
        0xa2 | 0xa6 | 0xb6 | 0xae | 0xbe => ("LDX", 2),
        0xa0 | 0xa4 | 0xb4 | 0xac | 0xbc => ("LDY", 2),
        0x85 | 0x95 | 0x8d | 0x9d | 0x99 | 0x81 | 0x91 => ("STA", 3),
        0x86 | 0x96 | 0x8e => ("STX", 3),
        0x84 | 0x94 | 0x8c => ("STY", 3),
        0xaa => ("TAX", 2),
        0xa8 => ("TAY", 2),
        0x8a => ("TXA", 2),
        0x98 => ("TYA", 2),
        0xba => ("TSX", 2),
        0x09 | 0x05 | 0x15 | 0x0d | 0x1d | 0x19 | 0x01 | 0x11 => ("ORA", 2),
        0x29 | 0x25 | 0x35 | 0x2d | 0x3d | 0x39 | 0x21 | 0x31 => ("AND", 2),
        0x49 | 0x45 | 0x55 | 0x4d | 0x5d | 0x59 | 0x41 | 0x51 => ("EOR", 2),
        0x69 | 0x65 | 0x75 | 0x6d | 0x7d | 0x79 | 0x61 | 0x71 => ("ADC", 2),
        0xe9 | 0xeb | 0xe5 | 0xf5 | 0xed | 0xfd | 0xf9 | 0xe1 | 0xf1 => ("SBC", 2),
        0x24 | 0x2c => ("BIT", 3),
        0xc9 | 0xc5 | 0xd5 | 0xcd | 0xdd | 0xd9 | 0xc1 | 0xd1 => ("CMP", 2),
        0xe0 | 0xe4 | 0xec => ("CPX", 2),
        0xc0 | 0xc4 | 0xcc => ("CPY", 2),
        0xe6 | 0xf6 | 0xee | 0xfe => ("INC", 5),
        0xc6 | 0xd6 | 0xce | 0xde => ("DEC", 5),
        0xca => ("DEX", 2),
        0x88 => ("DEY", 2),
        0xe8 => ("INX", 2),
        0xc8 => ("INY", 2),
        0x9a => ("TXS", 2),
        0x48 => ("PHA", 3),
        0x68 => ("PLA", 4),
        0x08 => ("PHP", 3),
        0x28 => ("PLP", 4),
        0x0a | 0x06 | 0x16 | 0x0e | 0x1e => ("ASL", 2),
        0x4a | 0x46 | 0x56 | 0x4e | 0x5e => ("LSR", 2),
        0x2a | 0x26 | 0x36 | 0x2e | 0x3e => ("ROL", 2),
        0x6a | 0x66 | 0x76 | 0x6e | 0x7e => ("ROR", 2),
        0x4c => ("JMP", 3),
        0x6c => ("JMP", 5),
        0x20 => ("JSR", 6),
        0x60 => ("RTS", 6),
        0x40 => ("RTI", 6),
        0xd0 => ("BNE", 2),
        0xf0 => ("BEQ", 2),
        0x90 => ("BCC", 2),
        0xb0 => ("BCS", 2),
        0x10 => ("BPL", 2),
        0x30 => ("BMI", 2),
        0x50 => ("BVC", 2),
        0x70 => ("BVS", 2),
        _ => return None,
    };
    Some(Instruction {
        opcode,
        mnemonic,
        cycles,
    })
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn decodes_supported_opcode_metadata() {
        let lda = decode(0xa9).unwrap();
        assert_eq!(lda.opcode, 0xa9);
        assert_eq!(lda.mnemonic, "LDA");
        assert_eq!(lda.cycles, 2);
        assert!(decode(0x02).is_none());
    }
}
